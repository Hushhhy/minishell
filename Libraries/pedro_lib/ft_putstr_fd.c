#include "pedro_lib.h"

void	ft_putstr_fd(char *s, int fd)
{
	unsigned int	x;

	if (!s)
		return ;
	x = 0;
	while (s[x] != '\0')
	{
		write(fd, &s[x], 1);
		x++;
	}
}
