#include "pedro_lib.h"

int	ft_lstsize(t_list *blst)
{
	t_list	*tmp;
	int		x;

	tmp = blst;
	x = 0;
	while (tmp)
	{
		tmp = tmp->next;
		x++;
	}
	return (x);
}
